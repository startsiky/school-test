  function randInt(min,max){ return Math.floor(Math.random()*(max-min+1))+min; }

  function buildOptionsForGenYear(q){
    const [min,max] = q.range;
    const opts = [];
    uniqPush(opts, q.correct);
    // generate 3 plausible distractors in same range, spaced/random
    let guard = 0;
    while(opts.length<4 && guard<200){
      guard++;
      // make distractors "close" sometimes
      const span = Math.max(10, Math.floor((max-min)*0.18));
      const near = Math.random() < 0.65;
      const v = near
        ? clampInt(q.correct + randInt(-span, span), min, max)
        : randInt(min, max);
      if(v !== q.correct) uniqPush(opts, v);
    }
    const asText = opts.map(v => formatYear(v, q.suffix));
    const shuffled = shuffle(asText);
    const correctText = formatYear(q.correct, q.suffix);
    return { options: shuffled, correctIndex: shuffled.indexOf(correctText) };
  }

  function clampInt(n,a,b){ return Math.max(a, Math.min(b,n)); }

  function formatYear(v, suffix){
    // for big years (like 24000), use spaces
    const s = String(v).replace(/\B(?=(\d{3})+(?!\d))/g, ' ');
    return `${s} ${suffix}`;
  }

