  // ----------------- Helpers to build questions -----------------
  function shuffle(arr){
    const a = arr.slice();
    for(let i=a.length-1;i>0;i--){
      const j = Math.floor(Math.random()*(i+1));
      [a[i],a[j]]=[a[j],a[i]];
    }
    return a;
  }

  function uniqPush(arr, v){ if(!arr.includes(v)) arr.push(v); }

