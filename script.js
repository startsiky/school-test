  function updateStats(item){
    item.best = Math.max(item.best || 0, item.last || 0);
    const scores = (item.attempts || []).map(a => a.score100);
    const avg = scores.length ? Math.round(scores.reduce((s,v)=>s+v,0)/scores.length) : 0;
    item.avg = avg;
  }

  function overallAvg(p){
    const all = [];
    Object.values(p.items || {}).forEach(it => (it.attempts||[]).forEach(a => all.push(a.score100)));
    return all.length ? Math.round(all.reduce((s,v)=>s+v,0)/all.length) : 0;
  }

