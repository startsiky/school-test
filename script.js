  // ----------------- Test engine -----------------
  function startTest(item, isLifehack=false){
    const test = item.test;
    if(!test || !test.stages || !test.stages.length){
      toast('–¢–µ—Å—Ç –ø–æ–∫–∞ –Ω–µ –≥–æ—Ç–æ–≤ üòÖ');
      return;
    }

    const stages = test.stages.map(st => ({
      title: st.title,
      questions: buildQuestions(st.questions || [], 10) // <=10 each stage
    }));

    const session = {
      itemTitle: item.title,
      isLifehack: !!isLifehack,
      stages,
      stageIndex: 0,
      stageTitle: '',
      questions: [],
      qIndex: 0,
      correctCount: 0,
      answered: false,
      stageResults: [],
      totalCorrect: 0,
      totalQuestions: 0,
    };

    initStage(session);

    state.testSession = session;
    state.screen = 'quiz';
    render();
  }

  function initStage(session){
    const st = session.stages[session.stageIndex];
    session.stageTitle = st.title;
    session.questions = st.questions;
    session.qIndex = 0;
    session.correctCount = 0;
    session.answered = false;
  }

  function buildQuestions(raw, limit){
    const list = shuffle(raw).slice(0, Math.min(limit, raw.length));
    return list.map(q => {
      if(q.type === 'genYear'){
        const built = buildOptionsForGenYear(q);
        return { prompt: q.prompt, options: built.options, correctIndex: built.correctIndex };
      }
      if(q.type === 'mcq'){
        const opts = q.options.slice();
        const correct = opts[q.correctIndex];
        const shuffled = shuffle(opts);
        return { prompt: q.prompt, options: shuffled, correctIndex: shuffled.indexOf(correct) };
      }
      // fallback
      return q;
    });
  }

  function finishTest(session){
    const score100 = session.totalQuestions ? Math.round((session.totalCorrect / session.totalQuestions) * 100) : 0;
    const label = scoreLabel(score100);

    // save progress (not for lifehacks? user asked average everywhere; we still save, but mark key)
    const p = loadProgress();
    const key = progressKey(session.isLifehack);
    if(key){
      const it = ensureItem(p, key);
      const prevBest = it.best || 0;
      it.last = score100;
      it.attempts.push({ date: new Date().toISOString(), score100, correct: session.totalCorrect, total: session.totalQuestions });
      updateStats(it);
      saveProgress(p);

      const diff = score100 - prevBest;
      const diffText = diff>0 ? ` (+${diff} –∫ –ª—É—á—à–µ–º—É)` : (diff===0 ? ' (–ø–æ–≤—Ç–æ—Ä–∏–ª –ª—É—á—à–∏–π)' : '');
      renderFinish(session, score100, label, it, diffText);
      return;
    }

    renderFinish(session, score100, label, null, '');
  }

